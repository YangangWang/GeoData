        private void OnXtraAgileTreeControlFocusedNodeChanged(object sender, DevExpress.XtraTreeList.FocusedNodeChangedEventArgs e)
        {
            if (e.Node != null)
            {
                object tag = e.Node.Tag;
                if (tag != null)
                {
                    if (tag is Unit)
                    {
                        Unit o = tag as Unit;
                        if (SelectedUnitChanged != null)
                        {
                            SelectedUnitChangedEventArgs eventArgs = new SelectedUnitChangedEventArgs();
                            eventArgs.Unit = o;
                            SelectedUnitChanged(this, eventArgs);
                        }
                    }
                    if (tag is Well)
                    {
                        Well o = tag as Well;
                        if (SelectedWellChanged != null)
                        {
                            SelectedWellChangedEventArgs eventArgs = new SelectedWellChangedEventArgs();
                            eventArgs.Well = o;
                            SelectedWellChanged(this, eventArgs);
                        }
                    }
                }

                XtraAgileTreeControl tree = sender as XtraAgileTreeControl;
                if (CanPublishFilteredData(tree, e.Node))
                {
                    if (FilteredDataChanged != null)
                    {
                        string filter = tree.Filter;

                        DataView dataSource = new DataView(_dataSource, filter, null, DataViewRowState.CurrentRows);

                        FilteredDataChanged(this, new FilteredDataChangedEventArgs() { FilteredData = dataSource });
                    }
                }
            }
        }


        private bool CanPublishFilteredData(XtraAgileTreeControl tree, TreeListNode node)
        {
            bool result = false;

            List<string> columnsCanPublishFilteredData = new List<string>();
            if (tree == _mainTree)
            {


            }

            Column column = tree.GetColumnByLevel(node.Level);
            if (column != null && columnsCanPublishFilteredData.Contains(column.Name))
            {
                result = true;
            }

            return result;
        }

